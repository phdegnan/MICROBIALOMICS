##
## 	Patrick Degnan
##	multi_load.pl
##	Upload list of new genome folders containing annotation data
##	Skip any that have accessions matching previously entered genome
##

unless(defined(@ARGV)){die "usage: multi_load.pl <list of folder names>\n";}

@acc=&ACCESSIONS;
#$howmany=@acc;
#print "this many genomes:$howmany\n";

open(IN,"<$ARGV[0]") or die "Cannot open $ARGV[0]\n";
while($l=<IN>){
	chomp($l);
	$upload="Y";
	if(-d $l){
		foreach $c (@acc){
			if($c eq $l){
				$upload="N";
				print "[$l] already in database\n";
				last;
			}
		}
		unless($upload eq "N"){
			$command="load_completed_genome.pl -a $l -dir $l -DB microbialomics_v2_Build2";
			print "$command\n";
			system($command);
		}
	}else{
		print "[$l] folder missing\n"; 
	}

#load_completed_genome.pl -a NZ_ACGB00000000 -dir NZ_ACGB00000000 -DB microbialomics_v2_Build2

}

